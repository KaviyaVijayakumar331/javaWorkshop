public class Elseif{
    public static void main(String args[]){
        int num=28;
        if(num>0)
        {
            System.out.println("Positive");
        }
        else if(num==0)
        {
            System.out.println("Neutral");
        }
        else
        {
            System.out.println("Negative");
        }
    }
}